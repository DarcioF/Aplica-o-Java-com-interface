    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
     Cliente cli= new Cliente();
      cli.setNome(txtNome.getText());
      cli.setCpf(txtCpf.getText());
      cli.setTelefone(txtTelefone.getText());
      cli.setEmail(txtEmail.getText());
          if(!txtCpf.getText().equals("   .   .   -  ") && !"".equals(txtNome.getText())){
             if(flag==1){
                 if(!cli.BuscarCPF(cli)){
                 cli.inserir(cli);
                 limparTela();
                 btnNovo.setEnabled(true);
                 btnSalvar.setEnabled(false);
                 btnSalvar.setEnabled(false);
                 btnCancelar.setEnabled(false);
                 btnLocalizar.setEnabled(true);
                 btnNovo.setEnabled(true);
                 desativarCam();
                }else{
                     JOptionPane.showMessageDialog(null,"J√° existe cliente cadastrado com esse CPF");
                  
                 }}
             if(flag==2){
                 cli.alterar(cli,id);
                 btnRemover.setEnabled(true);
                 btnSalvar.setEnabled(false);
                 btnCancelar.setEnabled(false);
                 btnLocalizar.setEnabled(true);
                 btnNovo.setEnabled(true);
                 desativarCam();
               }
         }else {
            JOptionPane.showMessageDialog(null,"Campo 'Nome' ou 'CPF' est√° vazio!");
         } 
       
           
    }//GEN-LAST:event_btnSalvarActionPerformed

